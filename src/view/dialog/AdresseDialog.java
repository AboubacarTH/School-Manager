    private void btn_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okActionPerformed
        adresseController.createTable();
        if (adresseController.get(1) == null) {
            adresseController.add(1, textField_adresse_ip.getText(), textField_port.getText(), textField_db.getText(), checkBox.isSelected());
        }
        if (textField_adresse_ip.getText().isEmpty() || textField_port.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez renseigner l'adresse de la base de donn√©es et le port ", "Champ vide !", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            adresseController.update(1, textField_adresse_ip.getText(), textField_port.getText(), textField_db.getText(), checkBox.isSelected());
        }
        dispose();
    }//GEN-LAST:event_btn_okActionPerformed

