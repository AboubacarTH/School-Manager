    private void action_ok_button() {
        if (text_field_nom_prenom.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir le nom et prénom ", "Champ vide ou manquant !", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (text_field_login.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir le login ", "Champ vide ou manquant !", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (user == null) {
            if (getController().getUserController().get(text_field_login.getText(), String.copyValueOf(password_field_mot_de_passe.getPassword())) != null) {
                JOptionPane.showMessageDialog(this, "Veuillez mettre un nouveau login ", "Champ existant ou erroné !", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (check_box_professeur.isSelected() && getController().getProfesseurController().get(text_field_login.getText()) == null) {
                JOptionPane.showMessageDialog(this, "Veuillez mettre le n° matricule du professeur dans le champ login ", "Champ existant ou erroné !", JOptionPane.ERROR_MESSAGE);
                return;
            }
            getController().getUserController().add(text_field_nom_prenom.getText(), text_field_login.getText(), String.copyValueOf(password_field_mot_de_passe.getPassword()));
            getController().getDroitController().add(getController().getUserController().get(text_field_login.getText(), String.copyValueOf(password_field_mot_de_passe.getPassword())).getId(), check_box_administrateur.isSelected(), check_box_professeur.isSelected(), check_box_ajout.isSelected(), check_box_modification.isSelected(), check_box_supression.isSelected(), check_box_s1.isSelected(), check_box_s2.isSelected(), check_box_s3.isSelected());

        } else {
            if (check_box_professeur.isSelected() && getController().getProfesseurController().get(text_field_login.getText()) == null) {
                JOptionPane.showMessageDialog(this, "Veuillez mettre le n° matricule du professeur dans le champ login ", "Champ existant ou erroné !", JOptionPane.ERROR_MESSAGE);
                return;
            }
            getController().getUserController().update(user.getId(), text_field_nom_prenom.getText(), text_field_login.getText(), String.copyValueOf(password_field_mot_de_passe.getPassword()));
            getController().getDroitController().update(user.getId(), check_box_administrateur.isSelected(), check_box_professeur.isSelected(), check_box_ajout.isSelected(), check_box_modification.isSelected(), check_box_supression.isSelected(), check_box_s1.isSelected(), check_box_s2.isSelected(), check_box_s3.isSelected());

        }
        JOptionPane.showMessageDialog(this, "Opération effectuée avec success ", "Réussie !", JOptionPane.INFORMATION_MESSAGE);
        dispose();
    }

