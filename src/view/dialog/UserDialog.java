    public UserDialog(java.awt.Frame parent, boolean modal, User user) {
        super(parent, modal);
        this.user = user;
        initComponents();
        text_field_nom_prenom.setText(this.user.getNom_prenom());
        text_field_login.setText(this.user.getLogin());
        password_field_mot_de_passe.setText("");
        if (isPrimaire()) {
            check_box_s1.setText("Trim. 1");
            check_box_s2.setText("Trim. 2");
            check_box_s3.setText("Trim. 3");
            check_box_s3.setVisible(true);
        } else {
            check_box_s1.setText("S1");
            check_box_s2.setText("S2");
            check_box_s3.setText("S3");
            check_box_s3.setVisible(false);
        }
        setDroit(getController().getDroitController().get(getUser().getId()));
        Droit droit = getController().getDroitController().get(this.user.getId());
        if (droit != null) {
            check_box_administrateur.setSelected(droit.isAdministration());
            check_box_professeur.setSelected(droit.isProfesseur());
            check_box_ajout.setSelected(droit.isAjouter());
            check_box_modification.setSelected(droit.isModifier());
            check_box_supression.setSelected(droit.isSuprimer());
            check_box_s1.setSelected(droit.isS1());
            check_box_s2.setSelected(droit.isS2());
            check_box_s3.setSelected(droit.isS3());
        }
        if (!getDroit().isSuprimer()) {
            password_field_mot_de_passe.setEnabled(false);
            check_box_administrateur.setEnabled(false);
            check_box_supression.setEnabled(false);
        }
    }

