    private void action_ok() {
        User user = getController().getUserController().get(getUser().getLogin(), String.valueOf(passwordField.getPassword()));
        if (user == null) {
            setCan_remove(false);
            JOptionPane.showMessageDialog(this, "Mot de passe incorrect ", "Authentification", JOptionPane.WARNING_MESSAGE);
        } else {
            setCan_remove(true);
        }
        dispose();
    }
