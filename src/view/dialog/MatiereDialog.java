    private void action_button_ok() {
        if (text_field_matiere.getText().isEmpty() || combo_box_type.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs ", "Champ vide ou manquant !", JOptionPane.WARNING_MESSAGE);
            return;
        }
        MatiereType matiereType = getController().getMatiereTypeController().get(combo_box_type.getSelectedItem().toString());
        if (matiere == null) {
            if (getController().getMatiereController().get(text_field_matiere.getText()) != null) {
                JOptionPane.showMessageDialog(this, "Veuillez mettre de nouvelles données ", "Champ existant ou erroné !", JOptionPane.ERROR_MESSAGE);
                return;
            }
            getController().getMatiereController().add(
                    text_field_matiere.getText(),
                    matiereType.getId(),
                    (int) spinner_priorite.getValue()
            );
        } else {
            getController().getMatiereController().update(
                    matiere.getId(),
                    text_field_matiere.getText(),
                    matiereType.getId(),
                    (int) spinner_priorite.getValue()
            );
        }
        JOptionPane.showMessageDialog(this, "Opération effectuée avec success ", "Réussie !", JOptionPane.INFORMATION_MESSAGE);
        //dispose();
    }

    private void init_combo_box_type() {
        combo_box_type.removeAllItems();
        getController().getMatiereTypeController().gets().forEach((i) -> {
            combo_box_type.addItem(i.getType());
        });
    }
